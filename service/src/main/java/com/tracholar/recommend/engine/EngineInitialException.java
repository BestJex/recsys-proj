package com.tracholar.recommend.engine;

public class EngineInitialException extends Exception {
    public EngineInitialException() {
        super();
    }

    public EngineInitialException(String message) {
        super(message);
    }

    public EngineInitialException(Throwable cause) {
        super(cause);
    }

    public EngineInitialException(String message, Throwable cause) {
        super(message, cause);
    }

}
